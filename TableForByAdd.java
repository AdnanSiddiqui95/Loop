public class TableForByAdd
{
      public static void main(String[] args)
	{ 
	  // Print table by adding up numbers of given number
	   int num=15;
	   int sum=num;
	   System.out.println(sum);
	    for(int ctr=0;ctr<9;ctr++)
		{	
		   sum+=num;
		   System.out.println(sum);
		}	
	}
}